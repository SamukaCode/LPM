    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(txtNome.getText().equals("")||txtPeso.getText().equals("")||txtIdade.getText().equals("")||txtEspecie.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Nenhum campo pode ficar vazio!");
        else {
            String nome = this.txtNome.getText();
             String especie = this.txtEspecie.getText();
            double peso = Double.parseDouble(this.txtPeso.getText());
            int idade = Integer.parseInt(this.txtIdade.getText());
            try {
                aControl.cadastrarAnimal(nome, especie, idade, peso);
            } catch (SQLException ex) {
                Logger.getLogger(FrmCadAnimal.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.txtCod.setText("");
            this.txtMarca.setText("");
            this.txtModelo.setText("");
            this.txtTipo.setText("");
            this.txtPreco.setText("");
            this.txtCod.requestFocus();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

