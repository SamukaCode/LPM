
package exgetset;


public class Professor {
    private String nome;
    private int cpf;
    private boolean fixo;
    
     public String getNome() {
        return nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public int getCpf() {
        return cpf;
    }
    
    public void setCpf(int cpf) {
        this.cpf = cpf;
    }
    
    public boolean getFixo() {
        return fixo;
    }
    
    public void setFixo(boolean fixo) {
        this.fixo = fixo;
    }
    
}
